package com.asum.xtoolbar.utils;

import android.graphics.Color;

/**
 * 顶部栏的配置信息
 * 
 * @author Asum
 *
 */
public class XTopBarConfig {
	/**
	 * 标题颜色，默认为黑色
	 */
	public static int TITLE_COLOR = Color.BLACK;

	/**
	 * 标题文字大小，默认为30像素
	 */
	public static double TITLE_TEXT_SIZE = 30;

	/**
	 * 功能按钮文字大小，默认为25像素
	 */
	public static double BUTTON_TEXT_SIZE = 25;

	/**
	 * 左右两边的padding值，默认为20像素
	 */
	public static double LEFT_RIGHT_PADDING = 20;

	/**
	 * 两个按钮的间距，默认为20像素
	 */
	public static double SPACEING = 20;

	/**
	 * Title是否居中，默认为false，为左边显示
	 */
	public static boolean TITLE_IN_CENTER = false;
}
